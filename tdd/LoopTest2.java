package tdd;

public class LoopTest2 {
            public static void main(String[] args) {
                int rows = 1;
                int columns = 4;

                for (int i = 1; i <= rows; i++) {
                    for (int j = 1; j <= columns; j++) {
                        System.out.println(4);
                        System.out.println(8);
                    }
                    System.out.println();
                }
            }
        }

